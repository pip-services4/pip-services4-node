const assert = require('chai').assert;

import { RandomFloat } from '../../src/random/RandomFloat';

suite('RandomFloat', ()=> {

    test('Next Float', () => {
    	let value = RandomFloat.nextFloat(5);
        assert.isTrue(value <= 5);
        
    	value = RandomFloat.nextFloat(2,5);
    	assert.isTrue(value <= 5 && value >= 2);
   });

    test('Update Float', () => {
    	let value = RandomFloat.updateFloat(0, 5);
        assert.isTrue(value <= 5 && value >= -5);
        
        value = RandomFloat.updateFloat(5, 0);  
        assert.isTrue(value >= 4.5 && value <= 5.5);

        value = RandomFloat.updateFloat(0);
        assert.isTrue(value == 0);
   });

});
